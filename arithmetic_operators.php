<?php
// Arithmetic Operators in PHP

$a = 15;
$b = 4;

// Addition
echo "Addition: " . ($a + $b) . "\n";
// Subtraction
echo "Subtraction: " . ($a - $b) . "\n";
// Multiplication
echo "Multiplication: " . ($a * $b) . "\n";
// Division
echo "Division: " . ($a / $b) . "\n";
// Modulus
echo "Modulus: " . ($a % $b) . "\n";
// Exponentiation
echo "Exponentiation: " . ($a ** $b) . "\n";
?>
