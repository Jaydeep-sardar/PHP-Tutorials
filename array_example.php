<?php
// Array in PHP

// Indexed array
$fruits = array("Apple", "Banana", "Orange");
echo "Indexed array:\n";
print_r($fruits);

// Associative array
$ages = array("John" => 25, "Alice" => 30, "Bob" => 22);
echo "\nAssociative array:\n";
print_r($ages);

// Multidimensional array
$matrix = array(
    array(1, 2, 3),
    array(4, 5, 6),
    array(7, 8, 9)
);
echo "\nMultidimensional array:\n";
print_r($matrix);
?>
