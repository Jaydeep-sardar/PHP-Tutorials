<?php
// Assignment Operators in PHP

$a = 10;

// Simple assignment
echo "a = $a\n";

// Add and assign
$a += 5;
echo "a += 5: $a\n";

// Subtract and assign
$a -= 3;
echo "a -= 3: $a\n";

// Multiply and assign
$a *= 2;
echo "a *= 2: $a\n";

// Divide and assign
$a /= 4;
echo "a /= 4: $a\n";

// Modulus and assign
$a %= 3;
echo "a %= 3: $a\n";
?>
