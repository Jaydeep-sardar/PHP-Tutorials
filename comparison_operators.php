<?php
// Comparison Operators in PHP

$a = 7;
$b = 5;

// Equal
echo "Equal (a == b): " . ($a == $b ? "true" : "false") . "\n";
// Identical
echo "Identical (a === b): " . ($a === $b ? "true" : "false") . "\n";
// Not equal
echo "Not Equal (a != b): " . ($a != $b ? "true" : "false") . "\n";
// Not identical
echo "Not Identical (a !== b): " . ($a !== $b ? "true" : "false") . "\n";
// Greater than
echo "Greater Than (a > b): " . ($a > $b ? "true" : "false") . "\n";
// Less than
echo "Less Than (a < b): " . ($a < $b ? "true" : "false") . "\n";
// Greater than or equal to
echo "Greater Than or Equal (a >= b): " . ($a >= $b ? "true" : "false") . "\n";
// Less than or equal to
echo "Less Than or Equal (a <= b): " . ($a <= $b ? "true" : "false") . "\n";
?>
