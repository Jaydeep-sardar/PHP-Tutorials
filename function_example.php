<?php
// Function in PHP

// Simple function to add two numbers
function add($a, $b) {
    return $a + $b;
}

// Calling the function
$result = add(5, 7);
echo "Sum of 5 and 7 is: $result\n";

// Function to display a greeting
function greet($name) {
    echo "Hello, $name!\n";
}

greet("John");
?>
