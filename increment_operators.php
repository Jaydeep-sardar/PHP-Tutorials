<?php
// Increment and Decrement Operators in PHP

$a = 5;

// Pre-increment
echo "Pre-increment (++a): " . (++$a) . "\n";
// Post-increment
echo "Post-increment (a++): " . ($a++) . "\n";
echo "Value after post-increment: $a\n";

// Pre-decrement
echo "Pre-decrement (--a): " . (--$a) . "\n";
// Post-decrement
echo "Post-decrement (a--): " . ($a--) . "\n";
echo "Value after post-decrement: $a\n";
?>
