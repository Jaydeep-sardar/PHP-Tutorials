<?php
// Switch statement in PHP

$day = "Tuesday";

switch ($day) {
    case "Monday":
        echo "Today is Monday.\n";
        break;
    case "Tuesday":
        echo "Today is Tuesday.\n";
        break;
    case "Wednesday":
        echo "Today is Wednesday.\n";
        break;
    case "Thursday":
        echo "Today is Thursday.\n";
        break;
    case "Friday":
        echo "Today is Friday.\n";
        break;
    default:
        echo "It's a weekend!\n";
        break;
}
?>
